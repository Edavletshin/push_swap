#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	i1;

	i = 0;
	i1 = 0;
	while (s1[i])
		i++;
	while (s2[i1] && i1 < n)
		s1[i++] = s2[i1++];
	s1[i] = '\0';
	return (s1);
}
